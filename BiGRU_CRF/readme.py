# 本项目将演示如何从用户提供的快递单中，抽取姓名、电话、省、市、区、详细地址等内容，形成结构化信息。
# 辅助物流行业从业者进行有效信息的提取，从而降低客户填单的成本。
# 此外，通过从快递单抽取信息这个任务，介绍序列化标注模型及其在 Paddle 的使用方式。

# PaddleNLP中的网络层BiGRU、CRF、ViterbiDecoder
# 通过paddlenlp.embedding的功能，热启动加载中文词向量，提升效果

# 如何从物流信息中抽取想要的关键信息呢？我们首先要定义好需要抽取哪些字段。
# 比如现在拿到一个快递单，可以作为我们的模型输入，例如“张三18625584663广东省深圳市南山区学府路东百度国际大厦”，
# 那么序列标注模型的目的就是识别出其中的“张三”为人名（用符号 P 表示），“18625584663”为电话名（用符号 T 表示），
# “广东省深圳市南山区百度国际大厦”分别是 1-4 级的地址（分别用 A1~A4 表示，可以释义为省、市、区、街道）。
# 这是一个典型的命名实体识别（Named Entity Recognition，NER）场景，各实体类型及相应符号表示见下表：

# 抽取实体/字段	符号	抽取结果
# 姓名	P	张三
# 电话	T	18625584663
# 省	A1	广东省
# 市	A2	深圳市
# 区	A3	南山区
# 详细地址	A4	百度国际大厦

# 我们可以用序列标注模型来解决快递单的信息抽取任务，下面具体介绍一下序列标注模型。
# 在序列标注任务中，一般会定义一个标签集合，来表示所以可能取到的预测结果。在本案例中，针对需要被抽取的“姓名、电话、省、市、区、详细地址”等实体，
# 标签集合可以定义为：
# label = {P-B, P-I, T-B, T-I, A1-B, A1-I, A2-B, A2-I, A3-B, A3-I, A4-B, A4-I, O}
# 每个标签的定义分别为：

# 标签	定义
# P-B	姓名起始位置
# P-I	姓名中间位置或结束位置
# T-B	电话起始位置
# T-I	电话中间位置或结束位置
# A1-B	省份起始位置
# A1-I	省份中间位置或结束位置
# A2-B	城市起始位置
# A2-I	城市中间位置或结束位置
# A3-B	县区起始位置
# A3-I	县区中间位置或结束位置
# A4-B	详细地址起始位置
# A4-I	详细地址中间位置或结束位置
# O	无关字符

# 注意每个标签的结果只有 B、I、O 三种，这种标签的定义方式叫做 BIO 体系，也有稍麻烦一点的 BIESO 体系，这里不做展开。
# 其中 B 表示一个标签类别的开头，比如 P-B 指的是姓名的开头；相应的，I 表示一个标签的延续。
# 注意到“张“，”三”在这里表示成了“P-B” 和 “P-I”，“P-B”和“P-I”合并成“P” 这个标签。这样重新组合后可以得到以下信息抽取结果：

# 对于句子“张三18625584663广东省深圳市南山区百度国际大厦”，

# 张三	18625584663	广东省	深圳市	南山区	百度国际大厦
# P	T	A1	A2	A3	A4





